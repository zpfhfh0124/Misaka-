unsigned soundManager::getSoundLength(string keyName)
{
	unsigned int temp = 0;

	arrSoundsIter iter = _mTotalSounds.begin();

	int count = 0;

	for (iter; iter != _mTotalSounds.end(); ++iter, count++)
	{
		if (keyName == iter->first)
		{
			(*_mTotalSounds[keyName])->getLength(&temp, FMOD_TIMEUNIT_MS);
			break;
		}
	}

	return temp;
}

